#ifndef LLVM_LIB_TARGET_ALTAIRX_INSTPRINTER_ALTAIRXINSTPRINTER_H
#define LLVM_LIB_TARGET_ALTAIRX_INSTPRINTER_ALTAIRXINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class AltairXInstPrinter : public MCInstPrinter {
public:
  AltairXInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                  const MCRegisterInfo &MRI);

  // Autogenerated by tblgen.
  std::pair<const char *, uint64_t> getMnemonic(const MCInst *MI) override;
  //void printInst(const MCInst *MI, uint64_t Address, unsigned OpIdx,
  //                      unsigned PrintMethodIdx, raw_ostream &OS);
  static const char *getRegisterName(MCRegister Reg, unsigned AltIdx);

  void printRegName(raw_ostream &OS, MCRegister Reg) const override;
  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &O) override;

  bool printAliasInstr(const MCInst *MI, uint64_t Address, raw_ostream &OS);
  void printCustomAliasOperand(const MCInst *MI, uint64_t Address,
                               unsigned OpIdx, unsigned PrintMethodIdx,
                               raw_ostream &OS);

  void printCondCode(const MCInst* MI, uint32_t OpIdx, raw_ostream& OS);
  void printSCMPCondCode(const MCInst* MI, uint32_t OpIdx, raw_ostream& OS);

private:
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O);
};
} // end namespace llvm

#endif // end LLVM_LIB_TARGET_ALTAIRX_INSTPRINTER_ALTAIRXINSTPRINTER_H
